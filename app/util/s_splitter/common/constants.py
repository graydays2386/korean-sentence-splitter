# '…고/…며/…면서/…자/…니/…는데/…지만' 류 연결어미는 의미절 후보 경계가 될 수 있음
CONNECTIVE_ENDINGS = [
    "고", "며", "면서", "자", "니", "는데", "지만", "더니", "다가", "아서", "어서", "여서", "으니", "니깐",
]

# 서술성 명사/구문 단서: “~함/~하기/~하는 것/~할 수”
# (LLM 단계에서 사건/발화/계획 등의 태깅을 더 정교히 할 예정이므로 Step1은 후보만 잘 뽑으면 됨)
NOMINAL_PREDICATE_PATTERNS = [
    re.compile(r".+함$"),          # ~함
    re.compile(r".+하기$"),        # ~하기
    re.compile(r".+하는\s+것$"),   # ~하는 것
    re.compile(r".+할\s+수$"),     # ~할 수
]

